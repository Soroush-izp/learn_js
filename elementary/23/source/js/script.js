// Bitwise Operator

var num1 = 12;
var num2 = 4;
var num3 = 3;

if (num1 * num3 === 10 || num1 * num3 === 11) {
  alert("ضربشون 10 یا 11 هست");
} else if (num2 * num3 === 14 || num2 * num3 === 12) {
  alert("ضربشون 14 یا 12 میشه");
} else if (num2 * num3 === 13 || num1 * num3 === 15) {
  alert("ضربشون 13 یا 15 میشه");
}

